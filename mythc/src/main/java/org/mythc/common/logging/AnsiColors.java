package org.mythc.common.logging;

/**
 * ANSI escape codes for console colors.
 */
public class AnsiColors {

    // Reset.
    public static final String RESET = "\u001B[0m";

    // Regular colors.
    public static final String BLACK = "\u001B[30m";
    public static final String RED = "\u001B[31m";
    public static final String GREEN = "\u001B[32m";
    public static final String YELLOW = "\u001B[33m";
    public static final String BLUE = "\u001B[34m";
    public static final String MAGENTA = "\u001B[35m";
    public static final String CYAN = "\u001B[36m";
    public static final String WHITE = "\u001B[37m";

    // Bold colors.
    public static final String BOLD_BLACK = "\u001B[30;1m";
    public static final String BOLD_RED = "\u001B[31;1m";
    public static final String BOLD_GREEN = "\u001B[32;1m";
    public static final String BOLD_YELLOW = "\u001B[33;1m";
    public static final String BOLD_BLUE = "\u001B[34;1m";
    public static final String BOLD_MAGENTA = "\u001B[35;1m";
    public static final String BOLD_CYAN = "\u001B[36;1m";
    public static final String BOLD_WHITE = "\u001B[37;1m";

    // Underline colors.
    public static final String UNDERLINE_BLACK = "\u001B[30;4m";
    public static final String UNDERLINE_RED = "\u001B[31;4m";
    public static final String UNDERLINE_GREEN = "\u001B[32;4m";
    public static final String UNDERLINE_YELLOW = "\u001B[33;4m";
    public static final String UNDERLINE_BLUE = "\u001B[34;4m";
    public static final String UNDERLINE_MAGENTA = "\u001B[35;4m";
    public static final String UNDERLINE_CYAN = "\u001B[36;4m";
    public static final String UNDERLINE_WHITE = "\u001B[37;4m";


    // Background colors.
    public static final String BACKGROUND_BLACK = "\u001B[40m";
    public static final String BACKGROUND_RED = "\u001B[41m";
    public static final String BACKGROUND_GREEN = "\u001B[42m";
    public static final String BACKGROUND_YELLOW = "\u001B[43m";
    public static final String BACKGROUND_BLUE = "\u001B[44m";
    public static final String BACKGROUND_MAGENTA = "\u001B[45m";
    public static final String BACKGROUND_CYAN = "\u001B[46m";
    public static final String BACKGROUND_WHITE = "\u001B[47m";

    // High intensity colors.
    public static final String HIGH_INTENSITY_BLACK = "\u001B[90m";
    public static final String HIGH_INTENSITY_RED = "\u001B[91m";
    public static final String HIGH_INTENSITY_GREEN = "\u001B[92m";
    public static final String HIGH_INTENSITY_YELLOW = "\u001B[93m";
    public static final String HIGH_INTENSITY_BLUE = "\u001B[94m";
    public static final String HIGH_INTENSITY_MAGENTA = "\u001B[95m";
    public static final String HIGH_INTENSITY_CYAN = "\u001B[96m";
    public static final String HIGH_INTENSITY_WHITE = "\u001B[97m";

    // Bold high intensity colors.
    public static final String BOLD_HIGH_INTENSITY_BLACK = "\u001B[90;1m";
    public static final String BOLD_HIGH_INTENSITY_RED = "\u001B[91;1m";
    public static final String BOLD_HIGH_INTENSITY_GREEN = "\u001B[92;1m";
    public static final String BOLD_HIGH_INTENSITY_YELLOW = "\u001B[93;1m";
    public static final String BOLD_HIGH_INTENSITY_BLUE = "\u001B[94;1m";
    public static final String BOLD_HIGH_INTENSITY_MAGENTA = "\u001B[95;1m";
    public static final String BOLD_HIGH_INTENSITY_CYAN = "\u001B[96;1m";
    public static final String BOLD_HIGH_INTENSITY_WHITE = "\u001B[97;1m";

}
